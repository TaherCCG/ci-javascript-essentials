// Arrays and Lists in JavaScript

let flowers = ['daisy', 'bluebell', 'buttercup', 'rose', 'tulip', 'daffodil', 'orchid', 'geranium', 'heather', 'hydrangea']
let flower = flowers[5]; // daffodil is the 6th element in the array
console.log(flowers); // prints the entire array
console.log(flower); // prints the 6th element in the array

// The output of the above code will be:
// [ 'daisy', 'bluebell', 'buttercup', 'rose', 'tulip', 'daffodil', 'orchid', 'geranium', 'heather', 'hydrangea' ]
// daffodil




