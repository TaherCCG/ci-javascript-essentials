# Calling Functions in Python

def say_hello():
    print("Hello")

say_hello() # prints Hello

# The say_hello() function is defined with the def keyword. The function name is say_hello. The parentheses after the function name are used to pass arguments to the function. The colon at the end of the first line is used to indicate the start of the function body. The function body is indented by four spaces. The function body contains a single line of code that prints the string "Hello". The function is called using the say_hello() syntax. When the function is called, the code inside the function body is executed, and the string "Hello" is printed to the console.

# The say_hello() function does not take any arguments. It simply prints the string "Hello" to the console. The function is called using the say_hello() syntax. When the function is called, the code inside the function body is executed, and the string "Hello" is printed to the console.

