// Curly Braces

// Curly braces are used to group statements together. They are used to define the beginning and end of a block of code. A block of code is a group of statements that are executed together. Curly braces are used in many places in JavaScript, such as in loops, conditionals, and functions.

// example:
let x = 2;
if (x > 0) {
    console.log("x is positive");
}
else {
    console.log("x is negative or zero");
}

// Creating objects and dictionaries, which contain key: value pairs of items:
// example:
let myObject = {name: "John", age: 30};
console.log(myObject.name); // John
console.log(myObject.age); // 30
console.log(myObject["name"]); // John

// Creating functions and methods:
// example:
function myFunction() {
    // do something
}

// Creating classes:
// example:
class MyClass {
    // do something
}

// Creating a block of code:
// example:
{
    // do something
}

// Creating a block of code:
// example:
for (let i = 0; i < 5; i++) {
    // do something
}
