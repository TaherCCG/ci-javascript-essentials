// Square Brackets

// Square brackets are used to access elements in an array or to access properties of an object. They are also used to create arrays and object literals.

// example:
let myArray = [1, 2, 3, 4, 5];
console.log(myArray[0]); // 1
console.log(myArray[1]); // 2
console.log(myArray[2]); // 3
console.log(myArray[3]); // 4

let myObject = {name: "John", age: 30};
console.log(myObject["name"]); // John
console.log(myObject["age"]); // 30
console.log(myObject.name); // John



