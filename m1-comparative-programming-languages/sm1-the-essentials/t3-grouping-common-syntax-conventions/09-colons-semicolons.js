// Colons & Semicolons in JavaScript

// Separate statements, such as in a for loop in JavaScript:
// example:
for (let i = 0; i < 5; i++) {
    // do something
}

// Separate key-value pairs in an object or dictionary:
// example:
let myObject = {name: "John", age: 30};
console.log(myObject.name); // John

// Mark the end of a statement in JavaScript:
// example:
let x = 2;
if (x > 0) {
    console.log("x is positive");
}
else {
    console.log("x is negative or zero");
}


