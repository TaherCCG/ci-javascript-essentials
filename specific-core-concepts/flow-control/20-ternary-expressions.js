// Ternary Expressions
/* Ternary expressions are a more concise way to write if-else statements. They are often used to assign 
variables or return values. Ternary expressions are also called conditional expressions. */

// javascript code
let a = 10;
let b = 20;
let result;

// Write your code below this line. 
// Ternary expression
result = a < b ? 'a is smaller' : 'a is not smaller';


console.log(result); // a is smaller