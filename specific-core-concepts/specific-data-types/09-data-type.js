// Determining the Data Type

// JavaScript is a loosely typed language, which means you do not have to declare the data type of a variable when you declare it. The data type of a variable is determined by the value you assign to it. You can use the typeof operator to determine the data type of a variable.

let itmOne = 10;
let itmTwo = '20';
let itmThree = true;

console.log(typeof itmOne); // number
console.log(typeof itmTwo); // string
console.log(typeof itmThree); // boolean
console.log(typeof (itmOne + itmTwo)); // string

