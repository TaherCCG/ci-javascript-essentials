// JavaScript-Specific Data Types - null and undefined
// JavaScript has two special data types, null and undefined, that are used to represent the absence of a value.

// null is an assignment value. It can be assigned to a variable as a representation of no value. 
// It is not the same as undefined, which is a type itself (undefined) and is a value that is automatically 
// assigned to a variable that has just been declared or to a variable that has not been assigned a value.

// javascript code
let admin = null;
let passwordRequired;

console.log(admin);  // null
console.log(passwordRequired);  // undefined