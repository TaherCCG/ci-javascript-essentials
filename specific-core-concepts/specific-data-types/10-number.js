// Data Types - Number
// 
// JavaScript has only one type of number. Numbers can be written with or without decimals.

let myStringInt = '10';
let myStringFloat = '1.275';

// Fix this code so that it adds correctly
let result = parseInt(myStringInt) + parseFloat(myStringFloat);
// parseInt() is a built-in JavaScript function that converts a string to an integer. parseFloat() is a built-in JavaScript function that converts a string to a floating-point number.

// This will print your results to the console
console.log(result); // 11.275