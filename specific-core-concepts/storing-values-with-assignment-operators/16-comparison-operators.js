// Comparing Values With Comparison Operators in JavaScript
// Comparison operators are used in logical statements to determine equality or difference between variables or values.

// javascript code
let varOne = 1===2;
console.log(varOne); // false

let varTwo = 1!==2;
console.log(varTwo); // true

let varThree = 1 > 2;
console.log(varThree); // false

let varFour = 1 <= 2;
console.log(varFour); // true