// Calling Functions

// Functions are called by using the function name followed by parentheses.
// The parentheses may contain arguments separated by commas.

function sayHello() {
    console.log("Hello");
}
sayHello()

function greet(name) {
    console.log("Hello " + name);
}
greet("John")

function add(a, b) {
    return a + b;
}
console.log(add(2, 3))

function subtract(a, b) {
    return a - b;
}   
console.log(subtract(5, 3))

